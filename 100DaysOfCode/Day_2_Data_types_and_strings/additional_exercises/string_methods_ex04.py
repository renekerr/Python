# String Methods, Exercise 4
# These are a few examples about how some string methods work

"""
index()
count()
center()
ljust()
rjust()
"""


print('Example_1')
print ("university".index("iv"))
print('\n')

print('Example_2')
my_str = "this is a test"
print (my_str.count('s',5))
print('\n')

print('Example_3')
print ('test'.center(10,'x'))
print('\n')

print('Example_4')
my_str = "Hello"
print (my_str.center(9,'z'))
print('\n')

print('Example_5')
my_str = "CSE"
print (my_str.ljust(7,'x'))
print('\n')

print('Example_6')
my_string = 'hello'
print (my_string.rjust(8,"A"))
print('\n')

