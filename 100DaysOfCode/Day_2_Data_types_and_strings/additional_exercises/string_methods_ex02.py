# String Methods, Exercise 2
# These are a few examples about how some string methods work
"""
isalpha()
isalnum()
endswith()
"""


print('Example_1')
print ("hello".isalpha())
print('\n')


print('Example_2')
my_str = "hello there"
print (my_str.isalpha())
print('\n')


print('Example_3')
my_str = "hello12"
print (my_str.isalpha())
print('\n')


print('Example_4')
my_str = "CSE1309"
print (my_str.isalnum())
print('\n')


print('Example_5')
my_str = "CSE-1309"
print (my_str.isalnum())
print('\n')


print('Example_6')
print ("bird".endswith("ir"))
print('\n')