# List Slicing Default Index, Exercise 1
# This program shows examples of how to slice a list using default indexes


#1
print('#1')
my_list=[24, 'car', 'tv', 5.3, 'apple', 36]
print(my_list[:3])
print('\n')

#2
print('#2')
my_list=[24 ,'car', 'tv', 5.3, 'apple', 36]
print(my_list[2:])
print('\n')

#3
print('#3')
my_list=[24, 'car', 'tv', 5.3, 'apple', 36]
print(my_list[1:3:])
print('\n')

#4
print('#4')
my_list=[24, 'car', 'tv', 5.3, 'apple', 36]
print(my_list[2::3])
print('\n')

#5
print('#5')
my_list=[24, 'car', 'tv', 5.3, 'apple', 36]
print(my_list[::2])
print('\n')

#6
print('#6')
my_list=[24, 'car', 'tv', 5.3, 'apple', 36]
print(my_list[::])
print('\n')