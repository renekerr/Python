# List Slicing With Step, Exercise 1
# This program shows examples of how to slice a list using steps

#1
print('#1')
my_list = ['python', 3.4, 54, 'java', 82, 'c', 7.4]
print(my_list[0:6:2])
print('\n')

#2
print('#2')
my_list = ['python', 3.4, 54, 'java', 82, 'c', 7.4]
print(my_list[6:0:-2])
print('\n')

#3
print('#3')
my_list=['python', 3.4, 54, 'java', 82, 'c', 7.4]
print(my_list[0:-1:3])
print('\n')

#4
print('#4')
my_list=['python', 3.4, 54, 'java', 82, 'c', 7.4]
print(my_list[-1:0:-1])
print('\n')



